#!/usr/bin/env bash

#===============================================================================
#
#   DESCRIPTION:  to be included by other shell scripts, rounding numbers
#
#===============================================================================

round() {
  local number="${1}"
  local percision="${2}"
  printf "%.${percision}f" "$(bc <<<"scale=${percision};(((10^${percision})*${number})+0.5)/(10^${percision})")"
}

#-------------------------------------------------------------------------------
#  Example usage
#-------------------------------------------------------------------------------
#
# round 1.5678 2
